package com.ecodation.dersler;

public class Ders022_KararMekanizmasi3 {

	public static void main(String[] args) {

		int a = 5;

		switch (a) {
			case 6:
				System.out.println("sayı 6");
				break;
			case 1:
				System.out.println("sayı 1");
				break;
			case 3:
				System.out.println("sayı 3");
				break;
			case 2:
				System.out.println("sayı 2");
				break;
			case 4:
				System.out.println("sayı 4");
				break;
			case 5:
				System.out.println("sayı 5");
				break;

			default:
				System.out.println("farklı bir sayı girildi");
				break;
		}

		/*
		 * if (a == 6) { System.out.println("sayı 6"); } else if (a == 4) { System.out.println("sayı 4"); } else if (a
		 * == 1) { System.out.println("sayı 1"); } else if (a == 2) { System.out.println("sayı 2"); } else if (a == 3) {
		 * System.out.println("sayı 4"); } else if (a == 5) { System.out.println("sayı 5.."); } else {
		 * System.out.println("Farklı bir sayıdır"); }
		 */
	}

}
